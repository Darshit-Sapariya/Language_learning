a = {1, 2, 3}
b = {3, 4, 5}

print(" This is a Union of Set a and b : ",a | b)   # union

print(" This is an Intersection of Set a and b : ",a & b)   # intersection

print(" This is a Difference of Set a and b : ",a - b)   # difference

print(" Is 2 present in Set a ? : ",2 in a)   # membership test

print(" Is 5 not present in Set a ? : ",3 not in a)   # membership test

print(" This is a Symmetric Difference of Set a and b : ",a ^ b)   # symmetric difference