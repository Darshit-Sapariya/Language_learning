Fruits=["Apple","Banana","Orange","Mango","Papaya","Kiwi","Grapes","Pineapple","Strawberry","Blueberry"]
print(Fruits)
print(Fruits[0]) 
print(Fruits[3]) 
print(Fruits[-1])

# sort list 
Fruits.sort()
print(Fruits)

# # reverse list
Fruits.reverse()
print(Fruits)

# # add item to list
Fruits.append("Watermelon")
print(Fruits)

# # insert item to list
Fruits.insert(5,"Dragonfruit")
print(Fruits) 

# pop item from list
Fruits.pop(2)    
print(Fruits)

# # remove item from list
Fruits.remove("Banana")
print(Fruits)